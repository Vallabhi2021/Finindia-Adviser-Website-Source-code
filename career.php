      <script>
        let currentJob = '';

        function openModal(jobTitle) {
            currentJob = jobTitle;
            document.getElementById('jobPosition').textContent = jobTitle;
            // set hidden input so server knows which job
            var jobInput = document.getElementById('jobInput');
            if (jobInput) jobInput.value = jobTitle;
            document.getElementById('applicationModal').style.display = 'block';
            document.body.style.overflow = 'hidden';
        }

        function closeModal() {
            document.getElementById('applicationModal').style.display = 'none';
            document.body.style.overflow = 'auto';
        }

        function validateApplicationForm(form) {
            const fullName = form.fullName.value.trim();
            const email = form.email.value.trim();
            const phone = form.phone.value.trim();
            const city = form.city.value.trim();
            const experience = form.experience.value;
            const qualification = form.qualification.value.trim();
            const resume = form.resume.files[0];

            if (!fullName) { alert('Please enter full name'); return false; }
            if (!isValidEmail(email)) { alert('Please enter a valid email'); return false; }
            if (!/^[0-9]{10}$/.test(phone)) { alert('Please enter a 10-digit numeric phone number'); return false; }
            if (!city) { alert('Please enter location/city'); return false; }
            if (!experience) { alert('Please select experience'); return false; }
            if (!qualification) { alert('Please enter qualification'); return false; }
            if (!resume) { alert('Please upload resume file (PDF/DOC/DOCX)'); return false; }
            const allowed = ['pdf','doc','docx'];
            const ext = resume.name.split('.').pop().toLowerCase();
            if (!allowed.includes(ext)) { alert('Resume must be PDF/DOC/DOCX'); return false; }
            if (resume.size > 5*1024*1024) { alert('Resume must be less than 5MB'); return false; }
            // ok to submit
            return true;
        }

        window.onclick = function(event) {
            const modal = document.getElementById('applicationModal');
            if (event.target == modal) {
                closeModal();
            }
        }
    </script>


